numero_dia = int(input("Ingrese un número del día de la semana (1-Domingo, 2-Lunes, ...): "))

if numero_dia == 1:
    print("Domingo")
elif numero_dia == 2:
    print("Lunes")
elif numero_dia == 3:
    print("Martes")
elif numero_dia == 4:
    print("Miércoles")
elif numero_dia == 5:
    print("Jueves")
elif numero_dia == 6:
    print("Viernes")
elif numero_dia == 7:
    print("Sábado")
else:
    print("Número de día inválido")
