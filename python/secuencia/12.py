estudiantes_menores = int(input("Ingrese la cantidad total de estudiantes menores a 20 años: "))
estudiantes_totales = int(input("Ingrese el total de estudiantes: "))
porcentaje_menores = (estudiantes_menores / estudiantes_totales) * 100
print("Porcentaje de estudiantes menores a 20 años:", porcentaje_menores, "%")