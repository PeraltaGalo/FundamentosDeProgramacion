#Porcentaje de Estudiantes Menores a 20 años
estudiantes_menores = int(input("Ingrese la cantidad de estudiantes menores a 20 años: "))
total_estudiantes = int(input("Ingrese el total de estudiantes: "))
porcentaje_menores = (estudiantes_menores / total_estudiantes) * 100

print(f"Porcentaje de estudiantes menores a 20 años: {porcentaje_menores}%")
